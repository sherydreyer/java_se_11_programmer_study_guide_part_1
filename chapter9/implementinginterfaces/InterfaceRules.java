package chapter9.implementinginterfaces;

public class InterfaceRules {
    //1. Interface variables are assumed to be public, static, and final  (constants).
    //2. Because interface variables are marked final, they must be initialized with a value when they are declared.
    //3. Interfaces include implicit modifiers (public),
    //4. Do not contain constructors.
    //5. Do not participate in the instance initialization process,
    //6. Support multiple inheritance.

}
